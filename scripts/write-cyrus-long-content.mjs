import fs from 'fs/promises';
import path from 'path';

const SOURCE = path.resolve('tmp/cyrus-long-sections.json');
const DEST = path.resolve('src/data/cyrusShepardLongContent.ts');

async function main() {
  const raw = await fs.readFile(SOURCE, 'utf8');
  const parsed = JSON.parse(raw);
  const sections = parsed.sections;
  if (!Array.isArray(sections)) {
    throw new Error('Missing sections array in source JSON');
  }

  const banner = `// Auto-generated on ${new Date().toISOString()} from tmp/cyrus-long-sections.json\n// Do not edit this file manually; update the generator or source JSON instead.\n`;

  const typeDef = 'export type CyrusLongSection = { id: string; title: string; summary: string; paragraphs: string[] };\n';
  const data = `export const cyrusLongSections: CyrusLongSection[] = ${JSON.stringify(sections, null, 2)};\n`;

  await fs.writeFile(DEST, banner + '\n' + typeDef + '\n' + data, 'utf8');
  console.log(`Wrote ${DEST}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
